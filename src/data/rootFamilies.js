function family(root, rootMeaning, words) {
  return {
    root: root.split(''),
    rootMeaning,
    words: words.map((w) => ({
      ...w,
      ar: wordText(w.segments),
      bare: stripHarakat(wordText(w.segments)),
      patternMeaning: PATTERN_MEANINGS[w.pattern],
    })),
  };
}

